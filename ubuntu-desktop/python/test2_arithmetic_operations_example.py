# Basic arithmetic operations
a = 5
b = 2
print("a + b =", a + b) # Addition
print("a - b =", a - b) # Subtraction
print("a * b =", a * b) # Multiplication
print("a / b =", a / b) # Division (returns a float)
print("a // b =", a // b) # Integer division (returns an integer)
print("a % b =", a % b) # Modulus (returns the remainder)

# Exponents and square roots
print("a ** b =", a ** b) # Exponentiation (5 raised to the power of 2)
print("sqrt(a) =", a ** 0.5) # Square root of a

# Converting between number types
x = 12.345
print(int(x)) # Convert a float to an integer (rounds down)
print(float(42)) # Convert an integer to a float
print(complex(1, 2)) # Create a complex number with real part 1 and imaginary part 2

# Built-in math functions (requires "import math")
import math
print("pi =", math.pi) # Get the value of pi
print("sin(pi/2) =", math.sin(math.pi/2)) # Sine function
print("log(10) =", math.log(10)) # Natural logarithm
